    private void llenar_label(){
        switch (this.jComboBox3.getSelectedIndex()) {
            case 0:
                switch (this.jComboBox4.getSelectedIndex()) {
                    case 0:
                        this.jLabel9.setText("5.1 : LIBRO DIARIO");
                        break;
                    case 1:
                        this.jLabel9.setText("5.3 : LIBRO DIARIO - DETALLE DEL PLAN CONTABLE UTILIZADO");
                        break;
                }
                break;
            case 1:
                switch (this.jComboBox4.getSelectedIndex()) {
                    case 0:
                        this.jLabel9.setText("5.2 : LIBRO DIARIO DE FORMATO SIMPLIFICADO");
                        break;
                    case 1:
                        this.jLabel9.setText("<html>5.4 : LIBRO DIARIO DE FORMATO SIMPLIFICADO - DETALLE DEL PLAN CONTABLE UTILIZADO</html>");
                        break;
                }
                break;
            case 3:
                switch (this.jComboBox4.getSelectedIndex()) {
                    case 0:
                        this.jLabel9.setText("8.1 : REGISTRO DE COMPRAS");
                        break;
                    case 1:
                        this.jLabel9.setText("<html>8.2 : REGISTRO DE COMPRAS - INFORMACIÓN DE OPERACIONES CON SUJETOS NO DOMICILIADOS</html>");
                        break;
                    case 2:
                        this.jLabel9.setText("<html>8.3 : REGISTRO DE COMPRAS SIMPLIFICADO");
                        break;
                }
                break;
            case 4:
                switch (this.jComboBox4.getSelectedIndex()) {
                    case 0:
                        this.jLabel9.setText("14.1 : REGISTRO DE VENTAS E INGESOS");
                        break;
                    case 1:
                        this.jLabel9.setText("<html>14.2 : REGISTRO DE VENTAS E INGRESOS SIMPLIFICADOS</html>");
                        break;
                }
                break;
        }
    }
    
