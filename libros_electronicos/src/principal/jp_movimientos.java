    private void jComboBox3ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox3ItemStateChanged
        // TODO add your handling code here:

        if (evt.getStateChange() == java.awt.event.ItemEvent.SELECTED) {
            if (this.jComboBox3.getSelectedIndex() == 2) {
                this.jComboBox4.setVisible(false);
                this.jLabel9.setText("6.1 : LIBRO MAYOR");
            } else {
                this.jComboBox4.setModel(this.modelo_libro.get(this.jComboBox3.getSelectedIndex()));
                this.jComboBox4.setVisible(true);
            }

        }
    }//GEN-LAST:event_jComboBox3ItemStateChanged

